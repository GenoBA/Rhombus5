import turtle
screen = turtle.Screen()
screen.screensize(500, 500)
screen.bgcolor('yellow')

t = turtle.Turtle()

t.penup()
t.goto(0, 0)
t.fillcolor('cyan')
t.begin_fill()
t.pendown()
t.speed(0)
t.setheading(135)
t.forward(90)
t.left(90)
t.forward(90)
t.left(90)
t.forward(90)
t.left(90)
t.forward(90)
t.end_fill()
t.penup()
t.goto(0,0)
t.fillcolor('brown')
t.begin_fill()
t.pendown()
t.setheading(45)
t.forward(90)
t.right(90)
t.forward(90)
t.right(90)
t.forward(90)
t.right(90)
t.forward(90)
t.end_fill()
t.penup()

t.hideturtle()

turtle.done()
